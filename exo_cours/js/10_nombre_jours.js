var mois = parseInt(prompt("Saissisez un chiffre entre 1 et 12"))

if(mois === 1){
    document.write("Le mois de janvier a 31 jours");
}else if(mois === 2){
    document.write("Le mois de février a 28 jours");
}else if(mois === 3){
    document.write("Le mois de mars a 31 jours");
}else if(mois === 4){
    document.write("Le mois d'avril a 30 jours");
}else if(mois === 5){
    document.write("Le mois de mai a 31 jours");
}else if(mois === 6){
    document.write("Le mois de juin a 30 jours");
}else if(mois === 7){
    document.write("Le mois de juillet a 31 jours");
}else if(mois === 8){
    document.write("Le mois d'août a 31 jours");
}else if(mois === 9){
    document.write("Le mois de septembre a 30 jours");
}else if(mois === 10){
    document.write("Le mois d'octobre a 31 jours");
}else if(mois === 11){
    document.write("Le mois de novembre a 30 jours");
}else if(mois === 12){
    document.write("Le mois de décembre a 31 jours");
}else{
    document.write("Erreur de saisie");
}